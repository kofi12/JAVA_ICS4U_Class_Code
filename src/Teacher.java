public class Teacher {
    private String name;
    private int age;
    double salary;

    public Teacher(){
        this.name = "Timmy Teacher";
        this.age = 45;
        this.salary = 50000;
    }

    public Teacher(String name, int age, double salary){
        this.name = name;
        this.age = age;
        this.salary = salary;
    }
}
